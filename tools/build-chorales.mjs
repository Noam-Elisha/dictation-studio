// Builds js/data/chorales-data.js from the kern corpus in tools/cache.
// Run: node tools/fetch-chorales.mjs && node tools/build-chorales.mjs
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { parseKern, KernError } from './kern-parser.mjs';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const KERN_DIR = path.join(ROOT, 'tools', 'cache', 'bach-370-chorales-master', 'kern');
const OUT = path.join(ROOT, 'js', 'data', 'chorales-data.js');

const STEP_NAMES = ['C', 'D', 'E', 'F', 'G', 'A', 'B'];
const ALTER_SUFFIX = { '-2': 'bb', '-1': 'b', 0: '', 1: '#', 2: 'x' };
const SHARP_ORDER = [3, 0, 4, 1, 5, 2, 6];
const FLAT_ORDER = [6, 2, 5, 1, 4, 0, 3];

function sigAccidentalMap(fifths) {
  const map = {};
  if (fifths > 0) for (let i = 0; i < fifths; i++) map[SHARP_ORDER[i]] = 1;
  if (fifths < 0) for (let i = 0; i < -fifths; i++) map[FLAT_ORDER[i]] = -1;
  return map;
}

// Phrase spans from soprano fermata note ends; validate clean slicing in all voices.
function extractPhrases(ch) {
  const soprano = ch.voices[0];
  const ends = soprano.filter((n) => n.flags & 4).map((n) => n.tick + n.dur);
  if (ends.length < 2) return { phrases: [], reason: 'fewer than 2 fermatas' };
  if (ends[ends.length - 1] !== ch.total) ends.push(ch.total);

  const phrases = [];
  let start = 0;
  for (const end of ends) {
    const span = { s: start, e: end };
    start = end;
    if (end - span.s < 96) continue; // degenerate
    let clean = true;
    for (const v of ch.voices) {
      let covered = 0;
      let startsAtS = false;
      for (const n of v) {
        if (n.tick < span.s && n.tick + n.dur > span.s) clean = false;
        if (n.tick < span.e && n.tick + n.dur > span.e) clean = false;
        if (n.tick === span.s) startsAtS = true;
        if (n.tick >= span.s && n.tick + n.dur <= span.e) covered += n.dur;
      }
      if (!startsAtS || covered !== span.e - span.s) clean = false;
      if (!clean) break;
    }
    if (clean) phrases.push(span);
  }
  return { phrases, reason: phrases.length ? null : 'no cleanly sliceable phrases' };
}

function phraseScore(ch, span) {
  const sigMap = sigAccidentalMap(ch.sigFifths ?? 0);
  let notes = 0;
  let chrom = 0;
  let short = 0;
  for (const v of ch.voices) {
    for (const n of v) {
      if (n.tick < span.s || n.tick >= span.e || n.step < 0) continue;
      notes++;
      if (n.alter !== (sigMap[n.step] || 0)) chrom++;
      if (n.dur < 24) short++;
    }
  }
  const sop = ch.voices[0].filter((n) => n.tick >= span.s && n.tick < span.e && n.step >= 0);
  let leaps = 0;
  for (let i = 1; i < sop.length; i++) {
    const semi = Math.abs(
      12 * sop[i].oct + [0, 2, 4, 5, 7, 9, 11][sop[i].step] + sop[i].alter -
      (12 * sop[i - 1].oct + [0, 2, 4, 5, 7, 9, 11][sop[i - 1].step] + sop[i - 1].alter)
    );
    if (semi > 2) leaps++;
  }
  const bars = (span.e - span.s) / ch.mlen;
  return (
    (3 * chrom) / Math.max(1, notes) +
    (1.5 * short) / Math.max(1, notes) +
    leaps / Math.max(1, sop.length - 1) +
    0.15 * Math.max(0, bars - 3)
  );
}

const files = readdirSync(KERN_DIR).filter((f) => f.endsWith('.krn')).sort();
const kept = [];
const rejected = [];

for (const f of files) {
  const id = f.replace('.krn', '');
  try {
    const ch = parseKern(readFileSync(path.join(KERN_DIR, f), 'utf8'), id);
    const { phrases, reason } = extractPhrases(ch);
    if (!phrases.length) {
      rejected.push({ id, reason });
      continue;
    }
    kept.push({ ch, phrases });
  } catch (e) {
    if (e instanceof KernError) rejected.push({ id, reason: e.message });
    else throw e;
  }
}

// Difficulty terciles across every phrase in the corpus.
const scored = [];
for (const k of kept) for (const span of k.phrases) scored.push(phraseScore(k.ch, span));
const sortedScores = scored.slice().sort((a, b) => a - b);
const t1 = sortedScores[Math.floor(sortedScores.length / 3)];
const t2 = sortedScores[Math.floor((2 * sortedScores.length) / 3)];

const list = kept.map(({ ch, phrases }) => ({
  id: ch.id,
  bwv: ch.bwv,
  title: ch.title,
  key: STEP_NAMES[ch.key.tonic.step] + ALTER_SUFFIX[ch.key.tonic.alter],
  mode: ch.key.mode,
  modal: ch.modal || undefined,
  sig: ch.sigFifths ?? 0,
  num: ch.num,
  den: ch.den,
  pickup: ch.pickup,
  total: ch.total,
  phrases: phrases.map((span) => {
    const sc = phraseScore(ch, span);
    return [span.s, span.e, sc <= t1 ? 0 : sc <= t2 ? 1 : 2];
  }),
  v: ch.voices.map((v) => v.map((n) => [n.step, n.alter, n.oct, n.dur, n.flags])),
}));

const banner = `// Generated by tools/build-chorales.mjs — do not edit by hand.
// Musical data derived from "370 Four-part Chorales by J.S. Bach" encoded by
// Craig Stuart Sapp (https://github.com/craigsapp/bach-370-chorales),
// licensed CC BY-NC-SA 4.0. The music itself is public domain.
`;
writeFileSync(OUT, `${banner}window.DS_CHORALES = ${JSON.stringify({ tpq: 48, list })};\n`);

const phraseCount = list.reduce((s, c) => s + c.phrases.length, 0);
const byDiff = [0, 0, 0];
for (const c of list) for (const p of c.phrases) byDiff[p[2]]++;
const meters = {};
for (const c of list) meters[`${c.num}/${c.den}`] = (meters[`${c.num}/${c.den}`] || 0) + 1;

console.log(`kept ${list.length}/${files.length} chorales, ${phraseCount} phrases`);
console.log(`difficulty buckets: easy ${byDiff[0]}, medium ${byDiff[1]}, hard ${byDiff[2]}`);
console.log(`meters: ${JSON.stringify(meters)}`);
console.log(`output: ${OUT} (${(readFileSync(OUT).length / 1024 / 1024).toFixed(2)} MB)`);
const reasons = {};
for (const r of rejected) reasons[r.reason.split(':')[0]] = (reasons[r.reason.split(':')[0]] || 0) + 1;
console.log('rejections:', JSON.stringify(reasons, null, 1));
